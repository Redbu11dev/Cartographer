function ConvertImportedDataToErrata()
    local newErrata = {}
    
    for zoneName, overlays in pairs(ImportedMapOverlayData) do
        newErrata[zoneName] = {}
        
        for _, dataString in ipairs(overlays) do
            -- Use string.find for Lua 5.0 compatibility
            local _, _, name, tw, th, ofx, ofy = string.find(dataString, "([^:]+):(%d+):(%d+):(%d+):(%d+)")
            
            if name then
                tw = tonumber(tw)
                th = tonumber(th)
                ofx = tonumber(ofx)
                ofy = tonumber(ofy)
                
                local num = tw + (th * 1024) + (ofx * 1048576) + (ofy * 1073741824)
                
                newErrata[zoneName][name] = num
            end
        end
    end
    
    -- THE FIX: Add the wildcard fallback to prevent nil indexing errors
    newErrata['*'] = {}
    
    return newErrata
end

